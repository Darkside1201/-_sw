# 1. 반지름 23을 r변수에 저장한다.
r = 23

# 2. 원주율 3.14를 pi 변수에 저장한다.
pi = 3.14

# 3. 원의 넓이를 계산한다.
#   원의 넓의를 계산하여 area 변수에 저장한다.
area = r * r * pi

# 원의 넓이를 출력한다.
print(area)
print("반지름이 23cm인 원의 넓이를", area, "cm입니다")
print("반지름이 23cm인 원의 넓이는 {}cm입니다." .format(area))
print("반지름이 23cm인 원의 넓이를 {}cm 입니다." . format(area))